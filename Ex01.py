# Exercício 1 – Área e Perímetro do Retângulo
# Faça um algoritmo que:
# Leia o valor da base e da altura de um retângulo.
# Calcule:
# a área
# o perímetro

# Mostre os resultados na tela.
# Fórmulas:
# Área = base × altura
# Perímetro = 2 × (base + altura)

base = float(input("Digite a base: "))
altura = float(input("Digite a altura: "))
area = base * altura
perimetro = 2 * (base + altura)


print("a área é ")
print(area)
print("\n\n\n\n") # \n  significa enter
print("o perimetro é ")
print(perimetro)
